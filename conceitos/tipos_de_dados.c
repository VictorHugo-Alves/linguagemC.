#include <stdio.h>

int main() {
    //Definindo as variáveis e os seus tipos
    int idade = 21;
    char inicial = 'V'; //char sempre em ''
    float preco = 19.99;

    //Imprimindo os valores das variáveis
    printf("Idade: %d\n",idade);
    printf("Inicial: %c\n", inicial);
    printf("Preço: %.2f\n", preco);

    //%d pra int
    //%c pra char
    //%f pra float

    return 0;
}
